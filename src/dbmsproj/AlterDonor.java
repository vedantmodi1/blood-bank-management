    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/miniproject","root","asdf1234");
            
            String sql = "insert into donor values (?,?,?,?,?,?,?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, Jidfield.getText());
            ps.setString(2, Jnamefield.getText());
            
            String sex="none";
            if(jRadioButton1.isSelected())
                sex = jRadioButton1.getText();
            if(jRadioButton2.isSelected())
                sex = jRadioButton2.getText();
            if(jRadioButton3.isSelected())
                sex = jRadioButton3.getText();
            ps.setString(3, sex);
            ps.setInt(4, Integer.parseInt(Jagefield.getText()));
            ps.setString(5, Jblood_grpfield.getText());
            if((Jphone_nofield.getText().length())>10)
            {
                JOptionPane.showMessageDialog(null, "Phone No. should be 10 digits.");
            }
            else
                ps.setString(6, Jphone_nofield.getText());
            ps.setString(7, Jhistoryfield.getText());
            
            ps.executeUpdate();
            Jidfield.setText("");
            Jnamefield.setText("");
            Jagefield.setText("");
            Jblood_grpfield.setText("");
            Jhistoryfield.setText("");
            Jphone_nofield.setText("");
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jRadioButton3.setSelected(false);
            JOptionPane.showMessageDialog(null, "Registration Successful " + Jidfield.getText(), "Successful", JOptionPane.PLAIN_MESSAGE);
            conn.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

