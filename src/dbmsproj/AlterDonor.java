    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/miniproject","root","asdf1234");

            String sql = "select * from donor where donor_id=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(Jidfield.getText()));

            ResultSet rs = ps.executeQuery();
            if (rs.next())
            {
                Jnamefield.setText(rs.getString("donor_name"));
                if(rs.getString("sex").equals("Male"))
                jRadioButton1.setSelected(true);
                if(rs.getString("sex").equals("Female"))
                jRadioButton2.setSelected(true);
                if(rs.getString("sex").equals("Other"))
                jRadioButton3.setSelected(true);
                Jagefield.setText(rs.getString("age"));
                Jblood_grpfield.setText(rs.getString("blood_grp"));
                Jphone_nofield.setText(rs.getString("ph_no"));
                Jhistoryfield.setText(rs.getString("donor_history"));
            }
            else
            JOptionPane.showMessageDialog(null, "No Record Found" , "Unuccessful", JOptionPane.ERROR_MESSAGE);

            conn.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

